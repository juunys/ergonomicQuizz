    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        String fname = jTextFieldFirstName.getText();
        String lname = jTextFieldLastName.getText();
        String username = jTextFieldUser.getText();
        String password = String.valueOf(jPasswordFieldPass.getPassword());
        String confirmPass = String.valueOf(jPasswordFieldConfirmPass.getPassword());
        String email = jTextFieldEmail.getText();

        if (password.equals(confirmPass)) {
            LoginForm login = new LoginForm();
            //Init i = new Init(fname, lname, username, password, email);
            String query = "insert into quizz.Login (fname, lname, username, password, email)"+"values(?, ?, ?, ?, ?)";

            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quizz", "root", "senh@3120");
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, fname);
                pst.setString(2, lname);
                pst.setString(3, username);
                pst.setString(4, password);
                pst.setString(5, email);
                pst.executeUpdate();
            }catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(RegisterForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(null, "Cadastrado!");
            this.dispose();
            login.setVisible(true);
            login.setTitle("Bem-Vindo(a)!");
            login.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(null, "As senhas não conferem!");
            return;
        }
    }//GEN-LAST:event_jButton1MouseClicked

